exports.getResponse = async (query, videoContext) => {
    try {
        // Step 1: Get embeddings for the query
        const embeddings = await this.getEmbeddings(query);

        // Step 2: Retrieve relevant documents based on the context
        let documents = await this.retrieveDocuments(embeddings, videoContext); // Use `let` instead of `const`

        // Step 3: If no documents are found, provide a default context
        if (documents.length === 0) {
            documents = ["RAG stands for Retrieval-Augmented Generation, a model architecture that combines retrieval-based and generative approaches."];
        }

        // Step 4: Generate a response using the retrieved documents
        const response = await this.generateResponse(query, documents);

        return response;
    } catch (error) {
        console.error('Error in getResponse:', error);
        return null;
    }
};